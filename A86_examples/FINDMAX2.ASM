; A PROGRAM TO PRINT MAX OF 5 USER INPUTS
code segment
GREET:
	MOV DX, OFFSET GREETING ; WELCOME THE USER WITH A MESSAGE
	MOV AH, 09
	INT 21h

	MOV DL, 10 		; PRINT NEW LINE ; MOVE TO NEXT LINE FOR USER INPUT
	MOV AH, 02
	INT 21h
	
	MOV CL, 00 		; SET COUNTER TO 0.
	MOV CH, 00 		; MAX WILL BE STORED HERE
INP:	
	MOV AH, 01 		; READ USER INPUT
	INT 21h
	INC CL			; INCREMENT LOOP COUNTER

	CMP AL, CH 		; COMPARE THE INPUT WITH EXISTING MAX
	JA SETCH 		; IF THE USER INPUT IS LARGER, UPDATE MAX
CHECK:
	CMP CL, 5 		; CHECK THE LOOP COUNTER
	JE OUTPUT 		; IF 5 ITERATIONS ARE ACHIEVED, PRINT THE MAX
	JMP INP 		; ELSE, ASK USER FOR MORE INPUT
	
SETCH:
	MOV CH, AL 		; UPDATE MAX 
	JMP CHECK 		; GO BACK TO ITERATION
	
OUTPUT:
	MOV DL, 10 		; PRINT NEW LINE BEFORE OUTPUT
	MOV AH, 02
	INT 21h

	MOV DX, OFFSET OUTPUTMSG ; PRINT GOODBYE
	MOV AH, 09
	INT 21H
	
	MOV DL, CH
	MOV AH, 02
	INT 21h 		; PRINT MAX

EXIT:
	MOV AH, 4Ch ; EXIT PROGRAM
	MOV AL, 0
	INT 21H
GREETING DB 'ENTER 5 DIGITS $'
OUTPUTMSG DB 'THE MAX IS: $'
code ends