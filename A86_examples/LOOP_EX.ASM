; A PROGRAM THAT READS AND PRINTS CONSOLE INPUT 5 TIMEES 
START:	
	MOV CL, 00
LOOPING:
	MOV AH, 01 	; READ INPUT FROM CONSOLE
	INT 21h 	; THE INPUT IS STORED IN AL

	MOV DL, AL 	; MOVE INPUT TO OUTPUT REGISTER
	MOV AH, 02 	; PREPARE FOR OUTPUT
	INT 21h 	; OUTPUT
	INC CL 		; INCREMENT THE LOOP COUNTER
	CMP CL, 5 	; CHECK IF 5 ITERATIONS ARE REACHED
	JB LOOPING 	; LOOP 
        ;CMP DL, 0Dh    ; CHECK IF INPUT CHAR IS ENTER INSTEAD. 
        ;JNE LOOPING    ; LOOP
	MOV AH, 4Ch 	; EXIT PROCEDURE
	MOV AL, 0
	INT 21h