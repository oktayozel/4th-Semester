; A PROGRAM TO STORE AND PRINT 5 INPUTS FROM USER
JMP GREET
INPUT DB 5 dup 0
GREETING DB 'ENTER 5 DIGITS $'
OUTPUTMSG DB 'THE INPUT IS: $'
GREET:
	MOV DX, OFFSET GREETING ; GREET USER
	MOV AH, 09
	INT 21h

	MOV DL, 10		; PRINT NEW LINE BEFORE READING INPUT
	MOV AH, 02
	INT 21h
	
	MOV CL, 00 		; SET LOOP COUNTER TO 0.
;	MOV BX, OFFSET INPUT
	MOV BX, 50h 		; SET AN ADDRESS TO USE AS ARRAY HEAD
	
INP:	
	MOV AH, 01 		; READ INPUT
	INT 21h
	INC CL	

	MOV [BX], AL 		; STORE INPUT TO THE POINTED ADDRESS
	ADD BX, 1 		; INCREMENT THE ADDRESS

CHECK:
	CMP CL, 5 		; CHECK LOOP END
	JE OUTPUT
	JMP INP	
OUTPUT:
	
	MOV DL, 10 		; PRINT NEW LINE FOR OUTPUT
	MOV AH, 02
	INT 21h

	MOV DX, OFFSET OUTPUTMSG ; PRINT GOODBYE
	MOV AH, 09
	INT 21H

GOBACK:
	SUB BX, 1 		; MOVE ARRAY POINTER TO THE BEGINNING
	DEC CL
	CMP CL, 0
	JA GOBACK

OL:
	MOV DL, [BX] 		; READ INPUT FROM MEMORY
	MOV AH, 02 		; PRINT THE ELEMENT
	INT 21h 		; DO THE PRINTING

	ADD BX, 1 		; MOVE TO NEXT ELEMENT
	INC CL 			; LOOP UNTIL 5
	CMP CL, 5
	JB OL

EXIT:
	MOV AH, 4Ch ; EXIT
	MOV AL, 0
	INT 21H
